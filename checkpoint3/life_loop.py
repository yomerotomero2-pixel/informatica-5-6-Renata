def main():
    day = input("How was your day, was it bad or good? ")
    option1 = "bad"
    option2 = "good"

    while True:
        if day == option1:
            print("Remember you are a Child of God!:)")
            break
        elif day == option2:
            print("Remember it was thanks to God!:)")
            break
    
# We all have bad days, and when I have a bad days I want to remember that I am a child of God and that I can handle all things with Him, and when I have a good day I want to remember that it was thanks to Him too.
main()


        
